# Operators:
# -> Arithmetic Operator:
print(1+2)
print(1-2)
print(1*2)
print(1/2)
print(1//2)
print(1%4)
print(4**3)
# -> Comparison Operator:
print(2<4)
print(8<=6)
print(2==3)
print(2!=3)
print(5>9)
print(9>=5)
# -> Logical Operator:
print(True or False)
print(1 or False)
print(6 and 0)
print(not 7)
print(not 0)
print(not False)
# -> Bitwise Operator:
print(23&20)
print(bin(23),bin(20)) # 0b is the notation telling it's binary number
print(11|88)
print(bin(11),bin(88))
print(~12)
print(~19)
print(~ -18)
a = 8
print(a>>2)
print(a<<2)
b = 80
print(a>>3)
print(bin(80))
c = 20
print(c<<2)
print(b>>2)
print(b<<2)
# -> Assignment Operator:
a = 4
a+=5
print(a)
